    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
//        processRequest(request, response);
        DAO dao = new DAO();
        request.setAttribute("skills", dao.getSkills());
        request.getRequestDispatcher("/View.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
//        processRequest(request, response);
        String raw_eid = request.getParameter("id");
        String raw_name = request.getParameter("name");
        String raw_gender = request.getParameter("gender");
        String raw_dob = request.getParameter("dob");

        int eid = Integer.parseInt(raw_eid);
        boolean gender = raw_gender.equals("male");
        Date dob = Date.valueOf(raw_dob);

        DAO dao = new DAO();
        Employee employee = new Employee(eid, raw_name, gender, dob);
        dao.setEmployee(employee);

        String[] raw_skillIds = request.getParameterValues("skill");
        if (raw_skillIds != null) {
            for (String raw_skillId : raw_skillIds) {
                dao.setEmployeeSkills(eid, Integer.parseInt(raw_skillId));
            }
        }
    }

